// 数値をカウントするための基本クラス
// 加算・リセット・取得などの基本的な操作を提供する
public class Counter {

    // ==============================
    // フィールド
    // ==============================

    // 現在のカウント値
    // C++でいう「メンバ変数 int m_Count」
    private int m_Count = 0;

    // ==============================
    // コンストラクタ
    // ==============================

    // カウントの初期値を指定して生成
    // 引数が0以上ならその値を採用、負の値なら0で初期化
    Counter(final int count) {
        if (count >= 0) {
            m_Count = count;
        } else {
            m_Count = 0; // マイナス値は受け付けず0でリセット
        }
    }

    // ==============================
    // 加算処理
    // ==============================

    // カウントを1増やす
    public void increment() {
        m_Count++;
    }

    // ==============================
    // リセット処理
    // ==============================

    // カウントを0にリセットする
    public void reset() {
        m_Count = 0;
    }

    // ==============================
    // Getter / 表示関連
    // ==============================

    // 現在のカウント値をString型で返す
    // デバッグや画面表示に利用できる
    @Override
    public String toString() {
        return "現在のカウント：" + m_Count;
    }

    // 現在のカウント値をintで返す（C++でいうGetter）
    public int getCount() {
        return m_Count;
    }
}
